module.exports = {
  'Filtering on search page': function (browser) {
    browser
      .url('http://localhost:8000/search?q=charles')
      .waitForElementVisible('body', 1000)
      .click('.searchtab:nth-of-type(2)')
      .pause(1000)
      .assert.urlEquals('http://localhost:8000/search/people?q=charles&page%5Bsize%5D=50&page%5Btype%5D=search')
      .click('#fb')
      .waitForElementVisible('.filter__box[value=artist]', 3000)
      .click('.filter__box[value=artist]')
      .pause(1000)
      .assert.urlEquals('http://localhost:8000/search/people?q=charles&page%5Btype%5D=search&filter%5Boccupation%5D=artist&page%5Bsize%5D=50')
      .assert.containsText('.resultcard', 'Charles Allingham')
      .click('.filter__box')
      .pause(1000)
      .assert.urlEquals('http://localhost:8000/search/people?q=charles&page%5Btype%5D=search&page%5Bsize%5D=50')
      .assert.containsText('.resultcard', 'prince')
      .pause(1000)
      .click('.filter__box[value=inventor]')
      .pause(1000)
      .assert.urlEquals('http://localhost:8000/search/people?q=charles&page%5Btype%5D=search&filter%5Boccupation%5D=inventor&page%5Bsize%5D=50')
      .click('.filter__box[value=inventor]')
      .pause(1000)
      .click('.filter__box[value="England, United Kingdom"]')
      .pause(1000)
      .assert.urlEquals('http://localhost:8000/search/people?q=charles&page%5Btype%5D=search&filter%5Bbirth%5Bplace%5D%5D=England%2C+United+Kingdom&page%5Bsize%5D=50')
      .click('.filter__box[value="England, United Kingdom"]')
      .pause(1000)
      .assert.urlEquals('http://localhost:8000/search/people?q=charles&page%5Btype%5D=search&page%5Bsize%5D=50')
      .click('.filter__box[value="England, United Kingdom"]')
      .pause(1000)
      .click('.filter__box[value=chemist]')
      .pause(1000)
      .assert.urlEquals('http://localhost:8000/search/people?q=charles&page%5Btype%5D=search&filter%5Boccupation%5D=chemist&filter%5Bbirth%5Bplace%5D%5D=England%2C+United+Kingdom&page%5Bsize%5D=50')
      .end();
  }
};
